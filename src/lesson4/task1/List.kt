fun decimal(digits: List<Int>, base: Int): Int = polynom(digits.reversed(), base)

/**
 * Сложная
 *
 * Перевести число, представленное цифровой строкой str,
 * из системы счисления с основанием base в десятичную.
 * Цифры более 9 представляются латинскими строчными буквами:
 * 10 -> a, 11 -> b, 12 -> c и так далее.
 * Например: str = "13c", base = 14 -> 250
 *
 * Использовать функции стандартной библиотеки, напрямую и полностью решающие данную задачу
 * (например, str.toInt(base)), запрещается.
 */
fun decimalFromString(str: String, base: Int): Int {
    val result = mutableListOf<Int>()
    for (i in str.indices) {
        if (str[i] >= 'a') result.add(10 + (str[i] - 'a'))
        else result.add(str[i] - '0')
    }
    return decimal(result, base)
}

/**
 * Сложная
 *
 * Перевести натуральное число n > 0 в римскую систему.
 * Римские цифры: 1 = I, 4 = IV, 5 = V, 9 = IX, 10 = X, 40 = XL, 50 = L,
 * 90 = XC, 100 = C, 400 = CD, 500 = D, 900 = CM, 1000 = M.
 * Например: 23 = XXIII, 44 = XLIV, 100 = C
 */
fun roman(n: Int): String {
    var x = n
    val result = mutableListOf<String>()
    val arabToRoman = mapOf(
        1000 to "M",
        900 to "CM",
        500 to "D",
        400 to "CD",
        100 to "C",
        90 to "XC",
        50 to "L",
        40 to "XL",
        10 to "X",
        9 to "IX",
        5 to "V",
        4 to "IV",
        1 to "I"
    )
    for ((arab, roman) in arabToRoman) {
        while (x >= arab) {
            x -= arab
            result.add(roman)
        }
    }
    return result.joinToString("")
}

/**
 * Очень сложная
 *
 * Записать заданное натуральное число 1..999999 прописью по-русски.
 * Например, 375 = "триста семьдесят пять",
 * 23964 = "двадцать три тысячи девятьсот шестьдесят четыре"
 */
fun russian(n: Int): String {
    val result = mutableListOf<String>()
    var x = n
    var thousand = 1
    while (thousand > 0) {
        if (x / 1000 > 0) {
            x = n / 1000
            thousand = 1
        } else {
            x = n % 1000
            thousand = 0
        }
        when (x / 100) {
            1 -> result.add("сто")
            2 -> result.add("двести")
            3 -> result.add("триста")
            4 -> result.add("четыреста")
            5 -> result.add("пятьсот")
            6 -> result.add("шестьсот")
            7 -> result.add("семьсот")
            8 -> result.add("восемьсот")
            9 -> result.add("девятьсот")
        }
        if (x % 100 in 11..19) {
            when (x % 100) {
                11 -> result.add("одиннадцать")
                12 -> result.add("двенадцать")
                13 -> result.add("тринадцать")
                14 -> result.add("четырнадцать")
                15 -> result.add("пятнадцать")
                16 -> result.add("шестнадцать")
                17 -> result.add("семнадцать")
                18 -> result.add("восемнадцать")
                19 -> result.add("девятнадцать")
            }
        } else {
            when (x / 10 % 10) {
                1 -> result.add("десять")
                2 -> result.add("двадцать")
                3 -> result.add("тридцать")
                4 -> result.add("сорок")
                5 -> result.add("пятьдесят")
                6 -> result.add("шестьдесят")
                7 -> result.add("семьдесят")
                8 -> result.add("восемьдесят")
                9 -> result.add("девяносто")
            }
            when (x % 10) {
                1 -> if (thousand == 1) result.add("одна") else result.add("один")
                2 -> if (thousand == 1) result.add("две") else result.add("два")
                3 -> result.add("три")
                4 -> result.add("четыре")
                5 -> result.add("пять")
                6 -> result.add("шесть")
                7 -> result.add("семь")
                8 -> result.add("восемь")
                9 -> result.add("девять")
            }
        }
        if (thousand == 1) {
            if (x % 100 / 10 != 1)
                when (x % 100 % 10) {
                    1 -> result.add("тысяча")
                    2, 3, 4 -> result.add("тысячи")
                    else -> result.add("тысяч")
                }
            else result.add("тысяч")
        }
    }
    return result.joinToString(" ")
}
